async function createTemplates() {
  console.log('ðŸš€ Iniciando creaciÃ³n de 10 plantillas de ejemplo...');
  console.log('ðŸ“‹ Las plantillas incluyen diferentes combinaciones de parÃ¡metros\n');

  const backendUrl = process.env.NEXT_PUBLIC_BACKEND_URL || 'http://localhost:3001';
  let successCount = 0;
  let errorCount = 0;

  for (let i = 0; i < sampleTemplates.length; i++) {
    const template = sampleTemplates[i];
    console.log(`ðŸ“ Creando plantilla ${i + 1}: ${template.name}...`);

    try {
      const response = await fetch(`${backendUrl}/template`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify(template),
      });

      if (!response.ok) {
        const error = await response.json();
        console.error(`âŒ Error: ${error.message || 'Error desconocido'}`);
        errorCount++;
      } else {
        const result = await response.json();
        console.log(`âœ… Creada exitosamente con ID: ${result.id}`);
        successCount++;
      }
    } catch (error) {
      console.error(`âŒ Error inesperado: ${error.message}`);
      errorCount++;
    }

    // PequeÃ±a pausa entre creaciones
    await new Promise(resolve => setTimeout(resolve, 300));
  }

  console.log('\n=== RESUMEN FINAL ===');
  console.log(`âœ… Plantillas creadas exitosamente: ${successCount}`);
  console.log(`âŒ Errores: ${errorCount}`);
  console.log(`ðŸ“Š Total procesadas: ${successCount + errorCount}`);
  
  if (successCount > 0) {
    console.log('\nðŸŽ‰ Â¡Plantillas creadas! Ahora puedes usarlas en el sistema de recepciones.');
  }
}

